public class Assignment56 {

	public static void main(String[] args) {
		/*
		 * Create a for loop that print out even numbers from 2 to 14 using if condition
		 * Must include the number 14 in the output
		 * Output:
		 * 2 4 6 ...14.
		 */

		for(int i=2; i<=14; i++) {
			if(i%2==0) {
				System.out.println(i);
		}
			
	}
	}
}
